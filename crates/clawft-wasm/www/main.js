// clawft-wasm Browser Test Harness
//
// Loads the WASM module built by wasm-pack and wires the UI controls
// to the exported init(), send_message(), and set_env() functions.

// -- DOM elements ----------------------------------------------------------

const configTextarea = document.getElementById("config-textarea");
const initBtn = document.getElementById("init-btn");
const chatDisplay = document.getElementById("chat-display");
const msgInput = document.getElementById("msg-input");
const sendBtn = document.getElementById("send-btn");
const statusIndicator = document.getElementById("status-indicator");
const statusText = document.getElementById("status-text");

// -- State -----------------------------------------------------------------

let wasmModule = null;
let initialized = false;

// Real API keys loaded from .env-keys.json (never displayed in full).
// Maps provider name → raw key string.
let envKeys = {};

// -- Helpers ---------------------------------------------------------------

function timestamp() {
  return new Date().toISOString().slice(11, 23);
}

function appendMessage(role, text) {
  const div = document.createElement("div");
  div.className = `msg ${role}`;

  const ts = document.createElement("div");
  ts.className = "ts";
  ts.textContent = timestamp();

  const body = document.createElement("div");
  body.textContent = text;

  div.appendChild(ts);
  div.appendChild(body);
  chatDisplay.appendChild(div);
  chatDisplay.scrollTop = chatDisplay.scrollHeight;
}

function setStatus(state, message) {
  statusIndicator.className = state;
  statusText.textContent = message;
}

function enableChat(enabled) {
  msgInput.disabled = !enabled;
  sendBtn.disabled = !enabled;
  if (enabled) {
    msgInput.focus();
  }
}

/** Obfuscate an API key for display: show first 6 and last 4 chars. */
function obfuscate(key) {
  if (!key || key.length < 12) return "****";
  return key.slice(0, 6) + "****" + key.slice(-4);
}

// -- Environment key injection ---------------------------------------------

/**
 * Load API keys from .env-keys.json (generated by `build.sh serve`).
 * Injects them into the config textarea with obfuscated display.
 */
async function loadEnvKeys() {
  try {
    const resp = await fetch("./.env-keys.json");
    if (!resp.ok) return;
    const keys = await resp.json();
    if (!keys || typeof keys !== "object" || Object.keys(keys).length === 0) return;

    envKeys = keys;

    // Parse the current textarea config and inject obfuscated keys.
    let config;
    try {
      config = JSON.parse(configTextarea.value);
    } catch {
      return;
    }

    if (!config.providers) config.providers = {};

    let injected = 0;
    for (const [provider, realKey] of Object.entries(keys)) {
      if (!realKey) continue;
      if (!config.providers[provider]) config.providers[provider] = {};
      // Store obfuscated version in textarea for display.
      config.providers[provider].apiKey = obfuscate(realKey);
      injected++;
    }

    if (injected > 0) {
      configTextarea.value = JSON.stringify(config, null, 2);
      appendMessage("system", `${injected} API key(s) loaded from environment (obfuscated in display)`);
    }
  } catch {
    // No .env-keys.json or fetch failed — use textarea defaults.
  }
}

/**
 * Build the real config JSON for WASM init by replacing obfuscated keys
 * with real ones from envKeys. If the user manually edited a key in the
 * textarea (doesn't match obfuscated pattern), their value is kept.
 */
function buildRealConfig() {
  const config = JSON.parse(configTextarea.value);

  if (config.providers && Object.keys(envKeys).length > 0) {
    for (const [provider, realKey] of Object.entries(envKeys)) {
      if (!realKey || !config.providers[provider]) continue;
      const displayKey = config.providers[provider].apiKey || "";
      // Replace if it matches the obfuscated pattern or a placeholder.
      if (displayKey === obfuscate(realKey)
          || displayKey.includes("****")
          || displayKey.startsWith("YOUR_")) {
        config.providers[provider].apiKey = realKey;
      }
      // Otherwise the user manually typed a key — keep theirs.
    }
  }

  // Auto-inject CORS proxy when served from the dev server.
  // The dev_server.py proxies /proxy/* requests to avoid browser CORS
  // restrictions. In production, providers that support browser-direct
  // (like Anthropic) don't need this; others need their own CORS proxy.
  if (window.location.hostname === "localhost" || window.location.hostname === "127.0.0.1") {
    // Force http:// — the dev server is plain HTTP. Some browsers auto-upgrade
    // to https:// which causes the proxy requests to timeout.
    const proxyBase = "http://" + window.location.host + "/proxy";
    if (config.providers) {
      for (const [, cfg] of Object.entries(config.providers)) {
        if (cfg && typeof cfg === "object" && !cfg.corsProxy) {
          cfg.corsProxy = proxyBase;
        }
      }
    }
  }

  return JSON.stringify(config);
}

// -- WASM loading ----------------------------------------------------------

async function loadWasm() {
  setStatus("loading", "loading wasm module...");

  const loadStart = performance.now();

  try {
    wasmModule = await import("./pkg/clawft_wasm.js");

    // wasm-bindgen generates a default initializer for the bg.wasm.
    if (typeof wasmModule.default === "function") {
      await wasmModule.default();
    }

    const loadMs = (performance.now() - loadStart).toFixed(1);
    console.log(`[clawft] wasm-load: ${loadMs}ms`);
    setStatus("", "wasm loaded, ready to initialize");
    appendMessage("system", `WASM module loaded in ${loadMs}ms`);
  } catch (err) {
    const loadMs = (performance.now() - loadStart).toFixed(1);
    console.error(`[clawft] wasm-load failed after ${loadMs}ms:`, err);
    setStatus("error", "failed to load wasm module");
    appendMessage("error", `Failed to load WASM: ${err.message || err}`);
    initBtn.disabled = true;
  }
}

// -- Initialize handler ----------------------------------------------------

async function handleInit() {
  if (!wasmModule) {
    appendMessage("error", "WASM module not loaded yet.");
    return;
  }

  // Validate JSON locally before sending to WASM.
  try {
    JSON.parse(configTextarea.value.trim());
  } catch (parseErr) {
    appendMessage("error", `Invalid JSON: ${parseErr.message}`);
    return;
  }

  // Build config with real (unobfuscated) API keys + CORS proxy.
  const configJson = buildRealConfig();

  // Log the config structure (keys redacted) to verify proxy injection.
  try {
    const dbg = JSON.parse(configJson);
    if (dbg.providers) {
      for (const [name, cfg] of Object.entries(dbg.providers)) {
        const hasKey = !!(cfg.apiKey && cfg.apiKey.length > 5);
        console.log(`[clawft] provider ${name}: key=${hasKey}, corsProxy=${cfg.corsProxy || "none"}`);
      }
    }
  } catch { /* ignore */ }

  initBtn.disabled = true;
  setStatus("loading", "initializing...");
  appendMessage("system", "Initializing clawft-wasm...");

  const initStart = performance.now();

  try {
    await wasmModule.init(configJson);

    const initMs = (performance.now() - initStart).toFixed(1);
    console.log(`[clawft] init: ${initMs}ms`);

    initialized = true;
    setStatus("ready", "initialized");
    appendMessage("system", `Initialized in ${initMs}ms. Ready to chat.`);
    enableChat(true);
    configTextarea.disabled = true;
  } catch (err) {
    const initMs = (performance.now() - initStart).toFixed(1);
    console.error(`[clawft] init failed after ${initMs}ms:`, err);
    setStatus("error", "initialization failed");
    appendMessage("error", `Init failed: ${err.message || err}`);
    initBtn.disabled = false;
  }
}

// -- Send handler ----------------------------------------------------------

async function handleSend() {
  if (!initialized || !wasmModule) return;

  const text = msgInput.value.trim();
  if (!text) return;

  msgInput.value = "";
  appendMessage("user", text);
  sendBtn.disabled = true;
  msgInput.disabled = true;

  const sendStart = performance.now();

  try {
    const response = await wasmModule.send_message(text);

    const sendMs = (performance.now() - sendStart).toFixed(1);
    console.log(`[clawft] send_message: ${sendMs}ms`);

    appendMessage("assistant", response);
  } catch (err) {
    const sendMs = (performance.now() - sendStart).toFixed(1);
    console.error(`[clawft] send_message failed after ${sendMs}ms:`, err);
    appendMessage("error", `Error: ${err.message || err}`);
  } finally {
    enableChat(true);
  }
}

// -- Event wiring ----------------------------------------------------------

initBtn.addEventListener("click", handleInit);
sendBtn.addEventListener("click", handleSend);

msgInput.addEventListener("keydown", (e) => {
  if (e.key === "Enter" && !e.shiftKey) {
    e.preventDefault();
    handleSend();
  }
});

// -- Boot ------------------------------------------------------------------

// Load env keys first (may update textarea), then load WASM.
loadEnvKeys().then(() => loadWasm());
